# include<stdio.h>


int main(){


    // assign value using assignment operator.



     int a = 10;
     a += 10;
     printf("a+10 = %d \n", a);
     a -= 10;
     printf("a-10 = %d \n", a);
     a *= 10;
     printf("a*10 = %d \n", a);
     a /= 10;
     printf("a/10 = %d \n", a);
     a %= 10;
    printf("a%c10 = %d \n", '%', a);

    return 0;

}
